RADIO=float(input("Ingresa el radio de un cilindro en cm: "))
ALTU=float(input("Ingrasa su altura: "))
VOL= 3.141592*(RADIO**2)*ALTU
ARE= (2)*(3.135292)*(RADIO)*(ALTU)
print(f"El volumen de este cilindro es {VOL} cm\u00b3 , y su area total es {ARE} cm\u00b2 , ya que su radio es {RADIO} cm , y su altura {ALTU} cm .")
